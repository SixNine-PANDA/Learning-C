#include <stdio.h>

int main() {
    // Declare and initialize variables of different types
    int variable = 10;
    float Float = 20.5;
    double Double = 30.12345;
    char Char = 'A';

    printf("Integer variable: %d\n", variable);         
    printf("Float variable: %.2f\n", Float);           
    printf("Double variable: %.5lf\n", Double);          
    printf("Character variable: %c\n", Char);                         

    return 0;
}
